def color(color_name):
    match(color_name):
        case "RESET":
            return "\033[0m"
        case "BOLD":
            return "\033[1m"
        case "GREEN":
            return "\033[1;32m"
        case "YELLOW":
            return "\033[1;33m"
        case "RED":
            return "\033[1;31m"
        case "CYAN":
            return "\033[1;36m"
        case "BLUE":
            return "\033[1;34m"
        case "WHITE":
            return "\033[1;37m"
        case "MAGENTA":
            return "\033[1;35m"
        case "GOLDEN":
            return "\033[38;5;220m"
        case "SILVER":
            return "\033[38;5;250m"
        case "BLACK":
            return "\033[1;30m"
        case _: 
            return "\033[0m"   